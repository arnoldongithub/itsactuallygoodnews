// Helper functions to filter stories by type
export const getViralStories = () => {
  return placeholderArticles.filter(article => article.viral_score >= 8);
};

export const getTrendingStories = () => {
  return placeholderArticles.filter(article => article.viral_score < 8 && article.viral_score >= 6);
};

export const getDailyReads = () => {
  return placeholderArticles.filter(article => article.viral_score < 6 && article.viral_score >= 4);
};

export const getBlindspotStories = () => {
  return placeholderArticles.filter(article => article.viral_score < 4);
};

export const getStoriesByCategory = (category) => {
  return placeholderArticles.filter(article => 
    article.category.toLowerCase().includes(category.toLowerCase())
  );
};

// Get all stories for general news feed
export const getAllStories = () => {
  return placeholderArticles;
};

// Get stories by ID
export const getStoryById = (id) => {
  return placeholderArticles.find(article => article.id === parseInt(id));
};

